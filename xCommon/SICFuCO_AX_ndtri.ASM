; http://www.moshier.net/#Cephes
; http://www.moshier.net/double.zip/ndtri.c
; inverse of normal distribution function

; ndtri(x) = sqrt(2)*erfi(2*x-1)
; ndtri(x) = sqrt(2)*erfci(2-2*x)

; y = a*(z + (z^3)*P0(z^2)/Q0(z^2))
;   = a*z*(1 + (z^2)*P0(z^2)/Q0(z^2))
;   = a*z*(1 + P0'(z^2)/Q0(z^2))
; z = x - 0.5
; a = sqrt(2*pi)

; y = u - z*loge(u) - z*P(z)/Q(z)
; u = sqrt(-2*loge(x))
; z = 1/u
; 0 < x < exp(-2)

; erfci(x) = (-1/sqrt(2))*ndtri(0.5*x)
; erfi(x)  = (1/sqrt(2))*ndtri(0.5*(x+1))
; erfi(x)  = erfci(1-x)

; - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

